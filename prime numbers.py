def addition (num1, num2):
    results = num1 + num2
    print("{0}+{1}={2}".format(num1,num2,results))

def subtraction (num1, num2):
    results = num1 - num2
    print("{0}-{1}={3}".format(num1,num2,results))

def multiplication (num1, num2):
    results = num1 * num2
    print("{0}*{1}={2}".format(num1,num2,results))

def division (num1, num2):
    if num2 ==0.0:
        print("INVALID CHOICE")
    else:
        results = num1 / num2
        print("{0}/{1}={2}".format(num1,num2,results))










